
public class Arthmetic_oprs {
    double squre(float opr1 ){
        return opr1*opr1;
    }
    double cube(float opr1){
        return opr1*opr1*opr1;
    }
    double module(float opr1,float opr2){
        return opr1 % opr2;
    }
    double power(float opr1,float opr2){
        return Math.pow(opr1, opr2);
    }

    
}