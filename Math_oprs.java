public class Math_oprs {
    double squre(float opr1){
        return Math.sqrt(opr1);
    }
    double Max(float opr1,float opr2){
        return Math.max(opr1,opr2);
    }
    double Min(float opr1,float opr2){
        return Math.min(opr1,opr2);
    }
    double power(float opr1,float opr2){
        return Math.pow(opr1,opr2);
    }
    double Round(float opr1){
        return Math.round(opr1);
    }
    double Absolute(float opr1){
        return Math.abs(opr1);
    }
}
